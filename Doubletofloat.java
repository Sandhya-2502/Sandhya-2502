public class Doubletofloat

   {
     public static void main (String[] args)
    
     {
           double d1 = 1.0;

           double d2 = 3.0;

           double d = d1/d2;

           System.out.println("double d = "+d);
      
           float f = (float)d;

           System.out.println("float f :"+f);

        }
     }